package ch03;

public class exam03_3 {
	public static void main(String[] args) {
		 int 피제수 = 23;         
		 int 제수 = 5;         
		 /* 몫 구하기 */
		 int 몫 = 피제수 / 제수;
		 
		 /* 나머지 구하기 */
		 int 나머지 = 피제수 % 제수;
		 System.out.println("몫 : " + 몫);         
		 System.out.println("나머지 : " + 나머지); 
	}
}